const BASE_DATE = { year: 2025, month: 4, day: 22 }; // 2025-05-22 UTC

export function ts(hour: number, minute: number, second = 0): Date {
  return new Date(
    Date.UTC(BASE_DATE.year, BASE_DATE.month, BASE_DATE.day, hour, minute, second),
  );
}

